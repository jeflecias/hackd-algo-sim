#define NGLYPH ((int)(sizeof(GLYPHS)/sizeof(GLYPHS[0])))

/* unknown printable -> a faint solid block so messages never silently blank out */
static const unsigned char UNKNOWN[7] = {0,0x0E,0x0E,0x0E,0x0E,0x0E,0};

static const unsigned char *glyph_rows(char c){
    if (c >= 'a' && c <= 'z') c = (char)(c - 'a' + 'A');
    for (int i = 0; i < NGLYPH; i++)
        if (GLYPHS[i].c == c) return GLYPHS[i].rows;
    if (c <= ' ') return GLYPHS[0].rows;   /* control/space -> blank */
    return UNKNOWN;
}

int microfont_pixel(char c, int col, int row){
    if (col < 0 || col > 4 || row < 0 || row > 6) return 0;
    const unsigned char *r = glyph_rows(c);
    return (r[row] >> (4 - col)) & 1;
}
